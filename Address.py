class Address:
    def __init__(self, index, city, street, house, apartment):  
        self.index = index       
        self.city = city          
        self.street = street      
        self.house = house        
        self.apartment = apartment 

    def print_index(self):        
        print(self.index)         

    def print_city(self):         
        print(self.city)          

    def print_street(self):       
        print(self.street)        

    def print_house(self):        
        print(self.house)         
    def print_apartment(self):    
        print(self.apartment)     